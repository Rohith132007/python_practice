n = int(input("Enter a number: "))

# if statement
if n > 0:
    print("Positive Number")

# if-else statement
if n % 2 == 0:
    print("Even Number")
else:
    print("Odd Number")

# for loop
print("Numbers from 1 to 5 using for loop:")
for i in range(1, 6):
    print(i)

# while loop
print("Numbers from 1 to 5 using while loop:")
i = 1
while i <= 5:
    print(i)
    i += 1